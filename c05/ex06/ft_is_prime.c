int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
	{
		return (0);
	}
	while (i < nb)
	{
		if (nb % i != 0)
		{
			i++;
		}
		else
		{
			return (0);
			break ;
		}
	}
	return (1);
}	
